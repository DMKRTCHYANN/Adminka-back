<script>
    document.addEventListener("DOMContentLoaded", function () {
        const contactForm = document.querySelector(".contact__form");

        contactForm.addEventListener("submit", async function (event) {
            event.preventDefault();
            const formData = new FormData(contactForm);

            try {
                const response = await fetch("https://www.x-tech.am/api/contact", {
                    method: "POST",
                    body: formData,
                    headers: {
                        "Accept": "application/json",
                    },
                });
                if (response.ok) {
                    document.querySelector(".contact__msg").style.display = "block";
                    document.querySelector(".contact__msg").textContent = "Ваше сообщение было успешно отправлено!";
                    contactForm.reset();
                } else {
                    document.querySelector(".contact__msg").style.display = "block";
                    document.querySelector(".contact__msg").textContent = "Ошибка! Пожалуйста, попробуйте снова.";
                }
            } catch (error) {
                console.error("Ошибка отправки запроса:", error);
                document.querySelector(".contact__msg").style.display = "block";
                document.querySelector(".contact__msg").textContent = "Ошибка подключения! Проверьте соединение с интернетом.";
            }
        });
    });
</script>
